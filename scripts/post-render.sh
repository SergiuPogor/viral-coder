#!/usr/bin/env bash
# Run after all videos are generated
# Extracts preview GIFs, uploads to GitHub release, updates README

set -e
cd /PROJECTS/LLM/viral-coder

VIDEOS_DIR="examples/videos"
GIFS_DIR="examples/gifs"
RELEASE_TAG="v1.0.6-examples"
REPO="SergiuPogor/viral-coder"
BASE_URL="https://github.com/${REPO}/releases/download/${RELEASE_TAG}"

mkdir -p "$GIFS_DIR"

echo "→ Extracting preview GIFs..."
for mp4 in "$VIDEOS_DIR"/*.mp4; do
  name=$(basename "$mp4" .mp4)
  gif="$GIFS_DIR/${name}.gif"
  echo "  $name..."
  # 4s clip from 8s mark, 540w, 12fps
  ffmpeg -y -ss 8 -t 4 -i "$mp4" \
    -vf "fps=12,scale=540:-1:flags=lanczos,split[s0][s1];[s0]palettegen=max_colors=64[p];[s1][p]paletteuse=dither=bayer" \
    "$gif" 2>/dev/null
done
echo "✓ GIFs done"

# Delete old release if exists
echo "→ Deleting old release..."
gh release delete "$RELEASE_TAG" --repo "$REPO" --yes 2>/dev/null || true

# Create new release with all MP4s + GIFs
echo "→ Creating GitHub release ${RELEASE_TAG}..."
gh release create "$RELEASE_TAG" \
  --repo "$REPO" \
  --title "Example Videos v1.0.6 — All Themes & Languages" \
  --notes "Example videos generated by viral-coder v1.0.6 with fixed font sizes (32-48px) and proper phone layout." \
  "$VIDEOS_DIR"/*.mp4 \
  "$GIFS_DIR"/*.gif

echo "✓ Release created"

# Build README examples section
echo "→ Updating README..."
cat > /tmp/readme_examples.md << EXAMPLES

