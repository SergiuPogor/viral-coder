# Replace existing Examples section in README
python3 - << 'PYEOF'
import re
with open('README.md', 'r') as f:
    content = f.read()
with open('/tmp/readme_examples.md', 'r') as f:
    new_section = f.read()
# Remove old examples section if exists
content = re.sub(r'\n## 🎬 Examples.*?(?=\n## |\Z)', '', content, flags=re.DOTALL)
# Insert after badges block
insert_after = '</p>\n'
idx = content.find(insert_after)
if idx != -1:
    content = content[:idx + len(insert_after)] + new_section + content[idx + len(insert_after):]
else:
    content = content + new_section
with open('README.md', 'w') as f:
    f.write(content)
print('README updated')
PYEOF

# Commit + push
git add -A
git add examples/gifs/ 2>/dev/null || true
