echo ""
echo "✅ ALL DONE!"
echo "   Release: https://github.com/${REPO}/releases/tag/${RELEASE_TAG}"
echo "   README:  https://github.com/${REPO}#-examples"
openclaw system event --text "Done: all viral-coder example videos + GIFs uploaded, README updated with inline previews" --mode now
